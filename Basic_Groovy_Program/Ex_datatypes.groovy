class Ex_datatypes{
    public static main(String[] args){
        //it is eg of an int datatype 
      int a = 15; 
		
      //it is eg of a long datatype 
      long b = 1000L; 
		
      //it is eg of a floating point datatype 
      float c = 13.78f; 
		
      //it is eg of a double datatype 
      double d = 17.5e40; 
		
      //it is eg of a BigInteger datatype 
      BigInteger bigi = 30g; 
		
      //it is eg of a BigDecimal datatype 
      BigDecimal bigd = 13.8g; 
		
      println(a); 
      println(b); 
      println(c); 
      println(d); 
      println(bigi); 
      println(bigd); 
    }
}
